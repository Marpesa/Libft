#include <libft.h>

int	ft_strncmp(const char *s1, const char *s2, int n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && i < n - 1)
		i++;
	if (s1[i] != s2[i])
		return (s1[i] - s2[i]);
	return (0);
}
