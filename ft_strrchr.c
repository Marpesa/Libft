#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	if (i >= 1 && c != '\0')
		i--;
	while (c != s[i])
	{
		if (s[i] == '\0')
			return (0);
		i--;
	}
	return ((char *)&s[i]);
}
